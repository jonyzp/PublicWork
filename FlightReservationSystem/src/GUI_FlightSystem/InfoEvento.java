    public boolean validarQueSePuedeGuardarOModificar(){
                        
        if(jTextFieldFechaI1.isEditable()&& jTextFieldFechaI2.isEditable() 
                && jTextFieldFechaF1.isEditable() && jTextFieldFechaF2.isEditable()
                && jTextFieldHoraI1.isEditable() && jTextFieldHoraI2.isEditable() 
                && jTextFieldHoraF1.isEditable() && jTextFieldHoraF2.isEditable()
                && jTextFieldTarifa.isEditable()){
            
            if(jComboBoxEventos.getSelectedIndex()==0 || jComboBoxUbicaciones.getSelectedIndex()==0
                || jTextFieldFechaI1.getText().equals("") || jTextFieldFechaI2.getText().equals("") || jTextFieldFechaI3.getText().equals("")
                || jTextFieldFechaF1.getText().equals("") || jTextFieldFechaF2.getText().equals("") || jTextFieldFechaF3.getText().equals("")
                || jTextFieldHoraI1.getText().equals("") || jTextFieldHoraI2.getText().equals("") 
                || jTextFieldHoraF1.getText().equals("") || jTextFieldHoraF2.getText().equals("")
                || jTextFieldTarifa.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Falta uno de los campos."); 
                return false;
            }
            int tarifa=-1;
            try{
                tarifa = Integer.parseInt(jTextFieldTarifa.getText());
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, "Datos en Cover evento incorrectos");            
            }
            return true;
        }else{
            jTextFieldFechaI1.setEditable(true);
            jTextFieldFechaI2.setEditable(true);
            jTextFieldFechaI3.setEditable(true);
            jTextFieldFechaF1.setEditable(true);
            jTextFieldFechaF2.setEditable(true);
            jTextFieldFechaF3.setEditable(true);
            jTextFieldHoraI1.setEditable(true);
            jTextFieldHoraI2.setEditable(true);
            jTextFieldHoraF1.setEditable(true);
            jTextFieldHoraF2.setEditable(true);
            jTextFieldTarifa.setEditable(true);
        }
        
        return false;
    }
    
    
    
    public void cargarInfoEnComboBoxes(){
        ResultSet rs = infoEvNegocio.retrieveNombreEvento();
        try {
            while (rs.next()) {
                jComboBoxEventos.addItem(rs.getString(1));
            }
        } catch (SQLException se) {
            se.printStackTrace();
        }
        rs = infoEvNegocio.retrieveUbicacion();
        try {
            while (rs.next()) {
                jComboBoxUbicaciones.addItem(rs.getString(1));
            }
        } catch (SQLException se) {
            se.printStackTrace();
        }
    }
    
    
    public void enviarPeticion(String accion){
        if(validarQueSePuedeGuardarOModificar()){
            String nombreEvento = jComboBoxEventos.getSelectedItem().toString();
            String ubicacion = jComboBoxUbicaciones.getSelectedItem().toString();
            
            
            String fIni = jTextFieldFechaI1.getText() + "-" + jTextFieldFechaI2.getText() + 
                    "-" + jTextFieldFechaI3.getText();
            System.out.println(fIni);
            String fFin = jTextFieldFechaF1.getText() + "-" + jTextFieldFechaF2.getText() + 
                    "-" + jTextFieldFechaF3.getText();;
            String hIni = jTextFieldHoraI1.getText() + ":" + jTextFieldHoraI2.getText();
            String hFin = jTextFieldHoraF1.getText() + ":" + jTextFieldHoraF2.getText();
            String tarifa = jTextFieldTarifa.getText();

            if (infoEvNegocio.insertOrUpdate(nombreEvento, ubicacion, fIni, fFin, hIni, hFin, tarifa, accion)) {
                if(accion.equals("insert")){
                    JOptionPane.showMessageDialog(null, "Se ha insertado la información del evento.");
                }else if(accion.equals("modify")){
                    JOptionPane.showMessageDialog(null, "Se ha modificado la información del evento.");
                }

                limpiarCampos();
            }else{
                if(accion.equals("insert")){
                    JOptionPane.showMessageDialog(null, "No se pudo insertar la información del evento.");
                }else if(accion.equals("modify")){
                    JOptionPane.showMessageDialog(null, "No se pudo modificar  modificar la información del evento.");
                }
            }

            actualizar();
            
        }
    }

