    private void jButtonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEliminarActionPerformed
        String nombreEvento = "";
        if(jComboBoxEventos.getSelectedIndex()!=0){
            nombreEvento = jComboBoxEventos.getSelectedItem().toString();
        }        
        limpiarCampos();
        
        if(nombreEvento.equals("")){
            JOptionPane.showMessageDialog(null, "Seleccione el nombre del evento del cual quiere eliminar la información.");

        }else{
            if(infoEvNegocio.delete(nombreEvento)){
                actualizar();
                JOptionPane.showMessageDialog(null, "Se ha eliminado la información del evento.");
            }else{
                JOptionPane.showMessageDialog(null, "No se pudo eliminar la información del evento.");
            }
        }
        
    }//GEN-LAST:event_jButtonEliminarActionPerformed

