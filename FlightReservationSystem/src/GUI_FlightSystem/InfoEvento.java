            jTextFieldFechaI3.setText("");
            jTextFieldFechaF1.setText("");
            jTextFieldFechaF2.setText("");
            jTextFieldFechaF3.setText("");
            jTextFieldHoraI1.setText("");
            jTextFieldHoraI2.setText("");
            jTextFieldHoraF1.setText("");
            jTextFieldHoraF2.setText("");
            jTextFieldTarifa.setText("");

            jTextFieldFechaI1.setEditable(false);
            jTextFieldFechaI2.setEditable(false);
            jTextFieldFechaI3.setEditable(false);
            jTextFieldFechaF1.setEditable(false);
