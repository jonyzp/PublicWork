    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(txtIdCategoria.isEditable()){
            txtIdCategoria.setText("");
            txtIdCategoria.setEditable(false);
        }else{
            if(!txtNombreCategoria.isEditable()){
                txtNombreCategoria.setEditable(true);
                return;
            }
            String nombreCategoria = txtNombreCategoria.getText();
            if(nombreCategoria.equals("")){
                JOptionPane.showMessageDialog(null, "No se pudo insertar la categoria");
            }else{
                if (cn.insertarCategorias(nombreCategoria)) {
                    actualizar();
                    JOptionPane.showMessageDialog(null, "Se ha insertado la categoria");
                }
            }
            limpiarDatos();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if(!txtIdCategoria.isEditable()){
            txtIdCategoria.setEditable(true);
        }else{
            if(txtNombreCategoria.isEditable()){
                txtNombreCategoria.setText("");
                txtNombreCategoria.setEditable(false);
                return;
            }
            int idCategoria=0;
            if(txtIdCategoria.getText().equals("") ){
                JOptionPane.showMessageDialog(null, "No hay categoria por eliminar o faltan datos.");
                return;
            }else{
                try{
                    idCategoria = Integer.parseInt(txtIdCategoria.getText());
                }catch(Exception e){
                    JOptionPane.showMessageDialog(null, "Datos en Id Categoria incorrectos");            
                }
            }
            if(cn.eliminarCategorias(idCategoria)){
                actualizar();
                JOptionPane.showMessageDialog(null, "Se ha eliminado la categoria");
            } else {
                JOptionPane.showMessageDialog(null, "No se pudo eliminar la categoria");
            }
            limpiarDatos();
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        if(txtIdCategoria.isEditable()){
            txtIdCategoria.setEditable(false);
            txtIdCategoria.setText("");
            return;
        }
        if(!txtNombreCategoria.isEditable()){
            txtNombreCategoria.setEditable(true);
            return;
        }
        if(txtNombreCategoria.getText().equals("")){
            JOptionPane.showMessageDialog(null, "No se pudo buscar la categoria");
            return;
        }
        if(!txtIdCategoria.isEditable()){
            busqueda(txtNombreCategoria.getText());
        }
    }//GEN-LAST:event_jButton5ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if(!txtIdCategoria.isEditable() || !txtNombreCategoria.isEditable()){
            txtIdCategoria.setEditable(true);
            txtNombreCategoria.setEditable(true);
            return;
        }
        if(txtIdCategoria.getText().equals("")|| txtNombreCategoria.getText().equals("")){
            JOptionPane.showMessageDialog(null, "No hay categoria por modificar o faltan datos.");
            return;
        }
        int idCategoria=-1;
        try{
            idCategoria = Integer.parseInt(txtIdCategoria.getText());
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Datos en Id Categoria incorrectos");            
        }
        String nombreCategoria = txtNombreCategoria.getText();
        if (cn.modificarCategorias(idCategoria, nombreCategoria)) {
            actualizar();
            JOptionPane.showMessageDialog(null, "Se ha modificado la categoria");
        } else {
            JOptionPane.showMessageDialog(null, "No se pudo modificar la categoria");
        }
        limpiarDatos();
    }//GEN-LAST:event_jButton3ActionPerformed

