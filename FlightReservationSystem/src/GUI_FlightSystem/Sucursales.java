    public void limpiarDatos() {
        txtIdSucursal.setText("");
        txtNombre.setText("");
        txtTelefono.setText("");
        txtDireccion.setText("");
        txtIdSucursal.setEditable(true);
        actualizar();
    }
    
    public void actualizar(){
        rs=sn.actualizarTabla();
        actualizarTabla(rs);
    }
    
    public void busqueda(String nombre){
        rs=sn.buscarSucursales(nombre);
        actualizarTabla(rs);
    }
    
    public void actualizarTabla(ResultSet rs){
        try{
            limpiarTabla();
            int i = 0;
            while (rs.next()) {
                jTable1.setValueAt(rs.getInt(1), i, 0);   
                jTable1.setValueAt(rs.getString(2), i, 1);   
                jTable1.setValueAt(rs.getString(3), i, 2); 
                jTable1.setValueAt(rs.getString(4), i, 3);
                jTable1.setValueAt(rs.getString(5), i, 4);
                i++;
            }
        }catch(SQLException se) {
            se.printStackTrace();
        }
    }
    
    public void limpiarTabla(){
        for(int i=0 ; i<jTable1.getRowCount() ; i++){
            for(int j=0 ; j<jTable1.getColumnCount() ; j++){
                jTable1.setValueAt("", i, j);
            }
        }
    }
    
    private void cargarNitEmpresa() {
        ResultSet rs = sn.cargarNitEmpresa();
        try {
            while (rs.next()) {
                cbxNitEmpresa.addItem(rs.getString(1));
            }
        } catch (SQLException se) {
            se.printStackTrace();
        }

    }
    
    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        if (!txtIdSucursal.isEditable()) {
            txtIdSucursal.setEditable(true);
            return;
        }
        String idSucursal=txtIdSucursal.getText();
        String nitEmpresa = (String) cbxNitEmpresa.getSelectedItem();
        String nombre = txtNombre.getText();
        String telefono = txtTelefono.getText();
        String direccion = txtDireccion.getText();
        if (nitEmpresa.equals("") || nombre.equals("") || telefono.equals("") || direccion.equals("")) {
            JOptionPane.showMessageDialog(null, "No se pudo modificado la sucursal");
        } else {
            int idSucur = Integer.parseInt(idSucursal);
            if (sn.modificarSucursales(idSucur, nitEmpresa, nombre, direccion, telefono)) {
                actualizar();
                JOptionPane.showMessageDialog(null, "Se ha modificado la sucursal");
            } else {
                JOptionPane.showMessageDialog(null, "No se pudo modificar la sucursal");
            }
            limpiarDatos();
        }
    }//GEN-LAST:event_jButton6ActionPerformed

    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        if(txtIdSucursal.isEditable()){
            txtIdSucursal.setText("");
            txtIdSucursal.setEditable(false);
        }else{
            String nitEmpresa = (String) cbxNitEmpresa.getSelectedItem();
            String nombre = txtNombre.getText();
            String telefono = txtTelefono.getText();
            String direccion = txtDireccion.getText();
            if(nitEmpresa.equals("") || nombre.equals("") || telefono.equals("") || direccion.equals("")){
                JOptionPane.showMessageDialog(null, "No se pudo insertar la sucursal");
            }else{
                if (sn.insertarSucursales(nitEmpresa, nombre, telefono, direccion)) {
                    actualizar();
                    JOptionPane.showMessageDialog(null, "Se ha insertado la sucursal");
                }
            }
            limpiarDatos();
        }
    }//GEN-LAST:event_jButton7ActionPerformed

