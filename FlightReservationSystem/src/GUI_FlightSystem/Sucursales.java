    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        if(!txtIdSucursal.isEditable()){
            txtIdSucursal.setEditable(true);
        }else{
            int idSucursal=0;
            if(txtIdSucursal.getText().equals("") ){
                JOptionPane.showMessageDialog(null, "No hay sucursal por eliminar o faltan datos.");
                return;
            }else{
                idSucursal = Integer.parseInt(txtIdSucursal.getText());
            }
            if(sn.eliminarSucursales(idSucursal)){
                actualizar();
                JOptionPane.showMessageDialog(null, "Se ha eliminado la sucursal");
            } else {
                JOptionPane.showMessageDialog(null, "No se pudo eliminar la sucursal");
            }
            limpiarDatos();
        }
    }//GEN-LAST:event_jButton9ActionPerformed

