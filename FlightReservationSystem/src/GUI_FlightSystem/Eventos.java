    public void limpiarCampos() {
        jTextFieldID.setText("");
        jTextFieldNombre.setText("");
        jTextFieldCosto.setText("");
        jTextFieldID.setText("");
        jTextFieldNombre.setText("");
        jTextFieldCosto.setText("");
        jTextFieldID.setEditable(false);
        jTextFieldNombre.setEditable(false);
        jTextFieldCosto.setEditable(false);
        jComboBox1.setSelectedIndex(0);
        actualizar();
    }
    
    public void actualizar(){
        rs=eventosNegocio.getDataSet();
        actualizarTabla(rs);
    }
    
    public void actualizarTabla(ResultSet rs){
        try{
            cleanJTable();
            int i = 0;
            while (rs.next()) {
                jTable1.setValueAt(rs.getString(1), i, 0);   
                jTable1.setValueAt(rs.getString(2), i, 1);                   
                jTable1.setValueAt(rs.getString(3), i, 2);
                jTable1.setValueAt(rs.getString(4), i, 3);
                i++;
            }
            if(i==0)
                JOptionPane.showMessageDialog(null, "No hay datos por mostrar."); 

        }catch(SQLException se) {
            se.printStackTrace();
        }
    }
    
    public void cleanJTable(){
        for(int i=0 ; i<jTable1.getRowCount() ; i++){
            for(int j=0 ; j<jTable1.getColumnCount() ; j++){
                jTable1.setValueAt("", i, j);
            }
        }
    }

    public boolean validarQueSePuedeBuscar(){
        jComboBox1.setSelectedIndex(0);
        if(jTextFieldID.isEditable()){
            jTextFieldID.setText("");
            jTextFieldID.setEditable(false);
            return false;
        }
        if(!jTextFieldNombre.isEditable()){
            jTextFieldNombre.setEditable(true);
            return false;
        }
        
        if(jTextFieldCosto.isEditable()){
            jTextFieldCosto.setText("");
            jTextFieldCosto.setEditable(false);
            return false;
        }
        
        if(jTextFieldNombre.getText().equals("")){
            JOptionPane.showMessageDialog(null, "No se pudo buscar el nombre de un Evento, el campo Nombre está vacío."); 
            return false;
        }
        return true;
    }
    
    public boolean validarQueSePuedeGuardar(){
        
        if(!jTextFieldNombre.isEditable() || !jTextFieldCosto.isEditable()){
            jTextFieldNombre.setEditable(true);
            jTextFieldCosto.setEditable(true);
            return false;
        }
        
        if(!jTextFieldCosto.isEditable()){
            jTextFieldCosto.setEditable(true);
            return false;
        }
        
        if(jTextFieldID.isEditable()){
            jTextFieldID.setText("");
            jTextFieldID.setEditable(false);
            return false;
        }
        if(jTextFieldNombre.getText().equals("")){
            JOptionPane.showMessageDialog(null, "No se pudo guardar evento, Falta el campo Nombre."); 
            return false;
        }
        
        if(jTextFieldCosto.getText().equals("")){
            JOptionPane.showMessageDialog(null, "No se pudo guardar el evento, Falta el campo Costo."); 
            return false;
        }
        
        if(jComboBox1.getSelectedIndex()==0){
            JOptionPane.showMessageDialog(null, "El campo Sucursal no ha sido seleccionado."); 
            return false;
        }
        
        try{
            int clause = Integer.parseInt(jTextFieldCosto.getText());
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "No ha ingresado bien el valor monetario."); 
        }
        return true;
    }
    
    
    
    public void cargarSucursalesEnCbx(){
        ResultSet rs = eventosNegocio.retrieveSucursales();
        try {
            while (rs.next()) {
                jComboBox1.addItem(rs.getString(1));
            }
        } catch (SQLException se) {
            se.printStackTrace();
        }
    }
