    private void jButtonAgregarbtnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAgregarbtnAgregarActionPerformed
        if(validarQueSePuedeGuardar()){
            String name = jTextFieldNombre.getText();
            String costo = jTextFieldCosto.getText();
            String suc = jComboBox1.getSelectedItem().toString();

            if (eventosNegocio.insert(suc, name, costo )) {
                JOptionPane.showMessageDialog(null, "Se ha insertado el Evento.");
            }else{
                JOptionPane.showMessageDialog(null, "No se pudo insertar el Evento.");
            }

            actualizar();
            limpiarCampos();
        }
    }//GEN-LAST:event_jButtonAgregarbtnAgregarActionPerformed

    private void jButtonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEliminarActionPerformed
        jComboBox1.setSelectedIndex(0);
        if(jTextFieldID.isEditable() && !jTextFieldNombre.isEditable() && !jTextFieldCosto.isEditable()){
            String identificacion=jTextFieldID.getText();
            if(identificacion.equals("") ){
                JOptionPane.showMessageDialog(null, "No hay Evento por eliminar o faltan datos.");
                return;
            }else{
                if(eventosNegocio.delete(identificacion)){
                    actualizar();
                    JOptionPane.showMessageDialog(null, "Se ha eliminado el Evento.");
                    limpiarCampos();
                } else {
                    JOptionPane.showMessageDialog(null, "No se pudo eliminar el Evento."+'\n'
                        + "Puede estar intentando borrar un dato que no se puede eliminar"+'\n'
                        + "debido a que otro registro en otra tabla depende de él.");
                }
            }
        }else{
            jTextFieldID.setEditable(true);
            jTextFieldNombre.setText("");
            jTextFieldNombre.setEditable(false);
            jTextFieldCosto.setEditable(false);
        }
    }//GEN-LAST:event_jButtonEliminarActionPerformed

    private void jButtonModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonModificarActionPerformed
        if(jTextFieldID.isEditable() && jTextFieldNombre.isEditable() && jTextFieldCosto.isEditable()){
            String id_evento=jTextFieldID.getText();
            String name = jTextFieldNombre.getText();
            String costo = jTextFieldCosto.getText();
            String suc = jComboBox1.getSelectedItem().toString();
            if(id_evento.equals("") || name.equals("") || costo.equals("")){
                JOptionPane.showMessageDialog(null, "Faltan datos.");
                return;
            }
            if(jComboBox1.getSelectedIndex()==0){
                JOptionPane.showMessageDialog(null, "Seleccione la Sucursal");
                return;
            }
            String tipocontr = jComboBox1.getSelectedItem().toString();
            if (eventosNegocio.update(id_evento, suc, name, costo)) {
                actualizar();
                JOptionPane.showMessageDialog(null, "Se ha modificado el Evento");
                limpiarCampos();
            } else {
                JOptionPane.showMessageDialog(null, "No se pudo modificar el Evento");
            }

        }else{
            jTextFieldID.setEditable(true);
            jTextFieldNombre.setEditable(true);
            return;
        }

    }//GEN-LAST:event_jButtonModificarActionPerformed

