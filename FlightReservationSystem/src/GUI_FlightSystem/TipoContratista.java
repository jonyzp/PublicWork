    public void limpiarDatos() {
        txtId_tipocontratista.setText("");
        txtTipocontratista.setText("");
        txtId_tipocontratista.setEditable(true);
        actualizar();
    }
    
    public void actualizar(){
        rs=tcn.actualizarTabla();
        actualizarTabla(rs);
    }
    
    public void busqueda(String tipocontratista){
        rs=tcn.buscarTipoContratista(tipocontratista);
        actualizarTabla(rs);
    }
    
    public void actualizarTabla(ResultSet rs){
        try{
            limpiarTabla();
            int i = 0;
            while (rs.next()) {
                jTable1.setValueAt(rs.getInt(1), i, 0);   
                jTable1.setValueAt(rs.getString(2), i, 1);
                i++;
            }
        }catch(SQLException se) {
            se.printStackTrace();
        }
    }
    
    public void limpiarTabla(){
        for(int i=0 ; i<jTable1.getRowCount() ; i++){
            for(int j=0 ; j<jTable1.getColumnCount() ; j++){
                jTable1.setValueAt("", i, j);
            }
        }
    }
    
