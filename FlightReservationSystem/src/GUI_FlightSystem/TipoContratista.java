    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if(!txtId_tipocontratista.isEditable()){
            txtId_tipocontratista.setEditable(true);
            return;
        }
        if(txtId_tipocontratista.getText().equals("")|| txtTipocontratista.getText().equals("")){
            JOptionPane.showMessageDialog(null, "No hay tipo de contratista por modificar o faltan datos.");
            return;
        }
        int id_tipocontratista = Integer.parseInt(txtId_tipocontratista.getText());
        String tipocontratista = txtTipocontratista.getText();
        if (tcn.modificarTipoContratista(id_tipocontratista, tipocontratista)) {
            actualizar();
            JOptionPane.showMessageDialog(null, "Se ha modificado el tipo de contratista");
        } else {
            JOptionPane.showMessageDialog(null, "No se pudo modificar el tipo de contratista");
        }
        limpiarDatos();
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if(!txtId_tipocontratista.isEditable()){
            txtId_tipocontratista.setEditable(true);
        }else{
            int id_tipoContratista=0;
            if(txtId_tipocontratista.getText().equals("") ){
                JOptionPane.showMessageDialog(null, "No hay tipo de contratista por eliminar o faltan datos.");
                return;
            }else{
                id_tipoContratista = Integer.parseInt(txtId_tipocontratista.getText());
            }
            if(tcn.eliminarTipoContratista(id_tipoContratista)){
                actualizar();
                JOptionPane.showMessageDialog(null, "Se ha eliminado el tipo de contratista");
            } else {
                JOptionPane.showMessageDialog(null, "No se pudo eliminar el tipo de contratista");
            }
            limpiarDatos();
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        if(txtId_tipocontratista.isEditable()){
            txtId_tipocontratista.setEditable(false);
            txtId_tipocontratista.setText("");
            return;
        }
        if(txtTipocontratista.getText().equals("")){
            JOptionPane.showMessageDialog(null, "No se pudo buscar el tipo de contratista");
            return;
        }
        if(!txtId_tipocontratista.isEditable()){
            busqueda(txtTipocontratista.getText());
        }
    }//GEN-LAST:event_jButton5ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(txtId_tipocontratista.isEditable()){
            txtId_tipocontratista.setText("");
            txtId_tipocontratista.setEditable(false);
        }else{
            String tipoContratista = txtTipocontratista.getText();
            if(tipoContratista.equals("")){
                JOptionPane.showMessageDialog(null, "No se pudo insertar el tipo de contratista");
            }else{
                if (tcn.insertarTipoContratista(tipoContratista)) {
                    actualizar();
                    JOptionPane.showMessageDialog(null, "Se ha insertado el tipo de contratista");
                }
            }
            limpiarDatos();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

