    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if(jTextFieldNroL.isEditable() && jTextFieldPrecio.isEditable() && jTextFieldPersonal.isEditable()){
            String id_evento=jTextFieldNroL.getText();
            String name = jTextFieldPrecio.getText();
            String costo = jTextFieldPersonal.getText();
            String suc = jComboBox1.getSelectedItem().toString();
            String contr = jComboBox2.getSelectedItem().toString();
            if(id_evento.equals("") || name.equals("") || costo.equals("")){
                JOptionPane.showMessageDialog(null, "Faltan datos.");
                return;
            }
            if(jComboBox1.getSelectedIndex()==0 || jComboBox2.getSelectedIndex()==0){
                JOptionPane.showMessageDialog(null, "Seleccione la Sucursal o Contratista");
                return;
            }
            if (ln.update(id_evento, contr, suc, name, costo)) {
                actualizar();
                JOptionPane.showMessageDialog(null, "Se ha modificado el Evento");
                limpiarCampos();
            } else {
                JOptionPane.showMessageDialog(null, "No se pudo modificar el Evento");
            }

        }else{
            jTextFieldNroL.setEditable(true);
            jTextFieldPrecio.setEditable(true);
            jTextFieldPersonal.setEditable(true);
            return;
        }
    }//GEN-LAST:event_jButton3ActionPerformed

