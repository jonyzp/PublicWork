private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
if(jTextFieldNroL.isEditable() && !jTextFieldPrecio.isEditable() && !jTextFieldPersonal.isEditable()){
            String identificacion=jTextFieldNroL.getText();
            if(identificacion.equals("") ){
                JOptionPane.showMessageDialog(null, "No hay información del Evento por eliminar o faltan datos.");
                return;
            }else{
                if(ln.delete(identificacion)){
                    actualizar();
                    JOptionPane.showMessageDialog(null, "Se ha eliminado el Evento.");
                    limpiarCampos();
                } else {
                    JOptionPane.showMessageDialog(null, "No se pudo eliminar el Evento."+'\n'
                        + "Puede estar intentando borrar un dato que no se puede eliminar"+'\n'
                        + "debido a que otro registro en otra tabla depende de él.");
                }
            }
        }else{
            jTextFieldNroL.setEditable(true);
            jTextFieldNroL.setText("");
            jTextFieldPersonal.setEditable(false);
            jTextFieldPrecio.setEditable(false);
        }    
}//GEN-LAST:event_jButton4ActionPerformed

