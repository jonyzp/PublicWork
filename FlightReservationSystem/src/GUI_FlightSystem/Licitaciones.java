    public void limpiarCampos() {
        jTextFieldNroL.setText("");
        jComboBox1.setSelectedIndex(0);
        jComboBox2.setSelectedIndex(0);
        jTextFieldPrecio.setText("");
        jTextFieldPersonal.setText("");
        jTextFieldNroL.setEditable(false);
        jTextFieldPrecio.setEditable(false);
        jTextFieldPersonal.setEditable(false);
        actualizar();
}
    
    public void actualizar(){
        rs=ln.getDataSet();
        actualizarTabla(rs);
    }

    public void actualizarTabla(ResultSet rs){
        try{
            cleanJTable();
            int i = 0;
            while (rs.next()) {
                jTable2.setValueAt(rs.getString(1), i, 0);   
                jTable2.setValueAt(rs.getString(2), i, 1);                   
                jTable2.setValueAt(rs.getString(3), i, 2);
                jTable2.setValueAt(rs.getString(4), i, 3);
                jTable2.setValueAt(rs.getString(5), i, 4);
                i++;
            }
            if(i==0)
                JOptionPane.showMessageDialog(null, "No hay datos por mostrar."); 

        }catch(SQLException se) {
            se.printStackTrace();
        }
    }

    public void cleanJTable(){
        for(int i=0 ; i<jTable2.getRowCount() ; i++){
            for(int j=0 ; j<jTable2.getColumnCount() ; j++){
                jTable2.setValueAt("", i, j);
            }
        }
    }
    
    
    public void cargarSucursalesEnCbx(){
        ResultSet rs = ln.retrieveSucursales();
        try {
            while (rs.next()) {
                jComboBox1.addItem(rs.getString(1));
            }
        } catch (SQLException se) {
            se.printStackTrace();
        }
    }
    
    public void cargarContratistasEnCbx(){
        ResultSet rs = ln.retrieveContratistas();
        try {
            while (rs.next()) {
                jComboBox2.addItem(rs.getString(1));
            }
        } catch (SQLException se) {
            se.printStackTrace();
        }
    }
    
    public boolean validarQueSePuedeGuardar(){
        
        if(!jTextFieldPrecio.isEditable() || !jTextFieldPersonal.isEditable()){
            jTextFieldPrecio.setEditable(true);
            jTextFieldPersonal.setEditable(true);
            return false;
        }
        
        if(!jTextFieldPersonal.isEditable()){
            jTextFieldPersonal.setEditable(true);
            return false;
        }
        
        if(jTextFieldNroL.isEditable()){
            jTextFieldNroL.setText("");
            jTextFieldNroL.setEditable(false);
            return false;
        }
        if(jTextFieldPrecio.getText().equals("")){
            JOptionPane.showMessageDialog(null, "No se pudo guardar Licitacion, Falta el campo Precio."); 
            return false;
        }
        
        if(jTextFieldPersonal.getText().equals("")){
            JOptionPane.showMessageDialog(null, "No se pudo guardar el Licitacion, Falta el campo Personal."); 
            return false;
        }
        
        if(jComboBox1.getSelectedIndex()==0){
            JOptionPane.showMessageDialog(null, "El campo Sucursal no ha sido seleccionado."); 
            return false;
        }
        
        return true;
    }
    
    public boolean validarQueSePuedeBuscar(){
        jComboBox1.setSelectedIndex(0);
        if(!jTextFieldNroL.isEditable()){
            jTextFieldNroL.setText("");
            jTextFieldNroL.setEditable(true);
            return false;
        }
        if(!jTextFieldPrecio.isEditable() || jTextFieldPersonal.isEditable()){
            jTextFieldPrecio.setEditable(true);
            jTextFieldPersonal.setText("");
            jTextFieldPersonal.setEditable(false);
            return false;
        }
        
        if(jTextFieldPersonal.isEditable()){
            jTextFieldPersonal.setText("");
            jTextFieldPersonal.setEditable(false);
            return false;
        }
        
        return true;
    }


